/* The first byte transmitted contains the slave address of the
 * receiving device (7 bits) and the Read/Write (R/W) bit
 * In this case, the R/W bit will be logic ?0?*/


#define OLED_WIDTH             128
#define OLED_HEIGHT            64

#define I2C_ADDRESS            0x3C<<1 // address = 0x3C and RW = 0 => 0x78
#define I2C_CMD                0x00
#define I2C_DATA               0x40

#define SETCONTRAST            0x81 // Set Contrast Control
#define DISPLAYALLON_RESUME    0xA4 // Set Entire Display On/Off
#define DISPLAYALLON           0xA5
#define NORMALDISPLAY          0xA6 // Set Normal/Inverse Display
#define INVERTDISPLAY          0xA7
#define DISPLAYOFF             0xAE // Set Display Off
#define DISPLAYON              0xAF // Turn on oled panel
#define SETDISPLAYOFFSET       0xD3 // Set Display Offset
#define SETCOMPINS             0xDA // Set COM Pins Hardware Configuration
#define SETVCOMDETECT          0xDB // Set VCOMH Deselect Level
#define SETDISPLAYCLOCKDIV     0xD5 // Set Display Clock Divide Ratio/Oscillator Frequency
#define SETPRECHARGE           0xD9 // Set Pre-Charge Period
#define SETMULTIPLEX           0xA8 // Set Multiplex Ratio
#define SETLOWCOLUMN           0x00
#define SETHIGHCOLUMN          0x10
#define SETSTARTLINE           0x40 // Set Display Start Line

#define MEMORYMODE             0x20 // adressing mode Horizontal=0x00/vertical=01
#define HORIZONTAL             0x00
#define VERTICAL               0x01
#define PAGE                   0x02

#define COLUMNADDR             0x21
#define PAGEADDR               0x22 // Number of page (line) of this OLED display
#define COMSCANINC             0xC0
#define COMSCANDEC             0xC8
#define SEGREMAP               0xA0
#define CHARGEPUMP             0x8D // Set Charge Pump

// Font
